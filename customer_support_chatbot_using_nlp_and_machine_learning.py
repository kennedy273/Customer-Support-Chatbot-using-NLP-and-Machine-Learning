import streamlit as st
import nltk
from nltk.corpus import stopwords
from nltk.stem import WordNetLemmatizer
from nltk.tokenize import word_tokenize

# Download NLTK resources
nltk.download("punkt")
nltk.download("stopwords")
nltk.download("wordnet")

# --- NLP Setup ---
lemmatizer = WordNetLemmatizer()
stop_words = set(stopwords.words("english"))

def preprocess(text):
    tokens = word_tokenize(text.lower())
    tokens = [lemmatizer.lemmatize(word) for word in tokens if word.isalpha() and word not in stop_words]
    return tokens

# Categories
categories = {
    "billing": ["refund", "payment", "invoice", "bill", "price", "premium"],
    "shipping": ["shipping", "delivery", "package", "order", "tracking"],
    "technical support": ["error", "crash", "bug", "issue", "problem", "device"],
    "pricing": ["price", "cost", "premium", "subscription", "plan", "difference", "compare"],
}

# Responses
responses = {
    "billing": "ğŸ’³ I understand you have a billing issue. Can you provide your order ID?",
    "shipping": "ğŸ“¦ Looks like a shipping issue. Can you share your tracking number?",
    "technical support": "ğŸ› ï¸ It looks like a technical issue. Can you tell me which device you are using?",
    "pricing": "ğŸ’° Our Premium plan is $10/month and Standard plan is $5/month.",
    "pricing_diff": "ğŸ“Š The Standard plan gives you basic features for $5/month, while Premium offers advanced features, faster support, and extra tools for $10/month.",
    "default": "ğŸ¤” Sorry, Iâ€™m not sure about that. Could you rephrase?",
}

# Classification
def classify(text):
    tokens = preprocess(text)

    # Special handling for pricing comparison
    if any(word in text.lower() for word in ["difference", "compare", "vs", "better"]):
        return "pricing_diff"

    scores = {cat: 0 for cat in categories}
    for cat, keywords in categories.items():
        for word in tokens:
            if word in keywords:
                scores[cat] += 1
    return max(scores, key=scores.get) if max(scores.values()) > 0 else "default"

# --- Streamlit App ---
st.set_page_config(page_title="Smart Chatbot", page_icon="ğŸ¤–", layout="centered")
st.title("ğŸ’¬ Customer Support Chatbot")

# Initialize session
if "messages" not in st.session_state:
    st.session_state.messages = []
if "awaiting_order_id" not in st.session_state:
    st.session_state.awaiting_order_id = False
if "awaiting_tracking_id" not in st.session_state:
    st.session_state.awaiting_tracking_id = False

# Show chat history
for sender, msg in st.session_state.messages:
    if sender == "You":
        st.markdown(f"**ğŸ§‘ You:** {msg}")
    else:
        st.markdown(f"**ğŸ¤– Bot:** {msg}")

# --- Chat input ---
user_input = st.chat_input("Type your message...")

if user_input:
    st.session_state.messages.append(("You", user_input))

    # --- Refund Order ID Flow ---
    if st.session_state.awaiting_order_id:
        if user_input.isdigit():
            st.session_state.messages.append(("Bot", "âœ… I have forwarded your refund request. You will get a notification in 2 - 3 days."))
            st.session_state.messages.append(("Bot", "â„¹ï¸ Is there anything else I can help you with?"))
            st.session_state.awaiting_order_id = False
        else:
            st.session_state.messages.append(("Bot", "âŒ That doesnâ€™t look like a valid order ID. Please enter a number."))

    # --- Shipping Tracking ID Flow ---
    elif st.session_state.awaiting_tracking_id:
        if user_input.isdigit():
            st.session_state.messages.append(("Bot", f"ğŸ“¦ Tracking ID {user_input} found. Your package is being processed."))
            st.session_state.messages.append(("Bot", "â„¹ï¸ Is there anything else I can help you with?"))
            st.session_state.awaiting_tracking_id = False
        else:
            st.session_state.messages.append(("Bot", "âŒ That doesnâ€™t look like a valid tracking number. Please enter digits only."))

    else:
        if user_input.lower() in ["quit", "exit"]:
            st.session_state.messages.append(("Bot", "ğŸ‘‹ Goodbye!"))
        else:
            category = classify(user_input)
            bot_response = responses.get(category, responses["default"])
            st.session_state.messages.append(("Bot", bot_response))

            # If refund detected â†’ wait for order ID next
            if category == "billing" and "refund" in user_input.lower():
                st.session_state.awaiting_order_id = True

            # If shipping detected â†’ wait for tracking ID next
            if category == "shipping":
                st.session_state.awaiting_tracking_id = True

    st.rerun()

# --- Quick Reply Buttons ---
st.subheader("Quick Replies")
col1, col2, col3, col4, col5 = st.columns(5)

if col1.button("ğŸ’³ Billing"):
    st.session_state.messages.append(("You", "I have a billing issue"))
    st.session_state.messages.append(("Bot", responses["billing"]))
    st.session_state.awaiting_order_id = True
    st.rerun()

if col2.button("ğŸ“¦ Shipping"):
    st.session_state.messages.append(("You", "Where is my package?"))
    st.session_state.messages.append(("Bot", responses["shipping"]))
    st.session_state.awaiting_tracking_id = True
    st.rerun()

if col3.button("ğŸ› ï¸ Technical"):
    st.session_state.messages.append(("You", "My app keeps crashing"))
    st.session_state.messages.append(("Bot", responses["technical support"]))
    st.rerun()

if col4.button("ğŸ’° Pricing"):
    st.session_state.messages.append(("You", "Tell me about your plans"))
    st.session_state.messages.append(("Bot", responses["pricing"]))
    st.rerun()

if col5.button("ğŸ“Š Compare Plans"):
    st.session_state.messages.append(("You", "What is the difference between your plans?"))
    st.session_state.messages.append(("Bot", responses["pricing_diff"]))
    st.rerun()

# --- Clear Chat Button ---
st.subheader("âš™ï¸ Controls")
if st.button("ğŸ—‘ï¸ Clear Chat"):
    for key in list(st.session_state.keys()):
        del st.session_state[key]
    st.success("Chat history cleared! Please start a new conversation.")
    st.rerun()

